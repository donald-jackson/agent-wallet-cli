export const ErrorCodes = {
  // Wallet errors
  ERR_WALLET_NOT_FOUND: 'ERR_WALLET_NOT_FOUND',
  ERR_WALLET_EXISTS: 'ERR_WALLET_EXISTS',
  ERR_WRONG_PASSWORD: 'ERR_WRONG_PASSWORD',
  ERR_INVALID_MNEMONIC: 'ERR_INVALID_MNEMONIC',

  // Session errors
  ERR_SESSION_EXPIRED: 'ERR_SESSION_EXPIRED',
  ERR_SESSION_INVALID: 'ERR_SESSION_INVALID',
  ERR_SESSION_NOT_FOUND: 'ERR_SESSION_NOT_FOUND',
  ERR_NO_TOKEN: 'ERR_NO_TOKEN',

  // Transaction errors
  ERR_INVALID_ADDRESS: 'ERR_INVALID_ADDRESS',
  ERR_INVALID_AMOUNT: 'ERR_INVALID_AMOUNT',
  ERR_INSUFFICIENT_BALANCE: 'ERR_INSUFFICIENT_BALANCE',
  ERR_INSUFFICIENT_ALLOWANCE: 'ERR_INSUFFICIENT_ALLOWANCE',
  ERR_TX_FAILED: 'ERR_TX_FAILED',
  ERR_TX_SIMULATION_FAILED: 'ERR_TX_SIMULATION_FAILED',

  // Network errors
  ERR_RPC_UNAVAILABLE: 'ERR_RPC_UNAVAILABLE',
  ERR_NETWORK_NOT_FOUND: 'ERR_NETWORK_NOT_FOUND',
  ERR_CHAIN_NOT_SUPPORTED: 'ERR_CHAIN_NOT_SUPPORTED',

  // Token errors
  ERR_TOKEN_NOT_FOUND: 'ERR_TOKEN_NOT_FOUND',
  ERR_TOKEN_ACCOUNT_NOT_FOUND: 'ERR_TOKEN_ACCOUNT_NOT_FOUND',

  // General errors
  ERR_INVALID_INPUT: 'ERR_INVALID_INPUT',
  ERR_FILE_PERMISSION: 'ERR_FILE_PERMISSION',
  ERR_INTERNAL: 'ERR_INTERNAL',
  ERR_EXPORT_NOT_CONFIRMED: 'ERR_EXPORT_NOT_CONFIRMED',
} as const;

export type ErrorCode = (typeof ErrorCodes)[keyof typeof ErrorCodes];

export class WalletError extends Error {
  public readonly code: ErrorCode;

  constructor(code: ErrorCode, message: string) {
    super(message);
    this.name = 'WalletError';
    this.code = code;
  }
}
